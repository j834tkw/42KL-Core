#include "../cub3d.h"

double	get_player_dir(char c)
{
	if (c == 'E')
		return (0);
	else if (c == 'N')
		return (90);
	else if (c == 'W')
		return (180);
	else if (c == 'S')
		return (270);
	return (0);
}
